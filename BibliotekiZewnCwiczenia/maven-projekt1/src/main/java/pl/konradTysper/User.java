package pl.konradTysper;

public class User {
    private String name;
    private int age;

    public User() {}

    public User(java.lang.String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}



